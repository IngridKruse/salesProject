    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        if(!validaCPF(tBoxCPF.getText())){
            JOptionPane.showMessageDialog(null, "CPF Inválido!");
            tBoxCPF.setText("");
            tBoxCPF.requestFocus();
        }else{
            vendedor = null;
            vendedor = daoVendedor.consultar(tBoxCPF.getText());

            if (vendedor == null){
                btnConsultar.setEnabled(false);
                btnIncluir.setEnabled(true);
                btnAlterar.setEnabled(false);
                btnExcluir.setEnabled(false);
            }
            else{
               tBoxNome.setText(vendedor.getNome());
               tBoxEndereco.setText(vendedor.getEndereco());
               tBoxCidade.setText(vendedor.getCidade());
               cBoxUF.setSelectedItem(vendedor.getUf());
               tBoxDDD.setText(vendedor.getDdd());
               tBoxTelefone.setText(vendedor.getTelefone());
               tBoxCEP.setText(vendedor.getCep());
               tBoxTaxaComissao.setText(String.valueOf(vendedor.getComissao()));
               tBoxSalBase.setText(String.valueOf(vendedor.getSalarioBase()));

               btnConsultar.setEnabled(false);
               btnIncluir.setEnabled(false);
               btnAlterar.setEnabled(true);
               btnExcluir.setEnabled(true);
            }
            tBoxCPF.setEnabled(false);
            tBoxNome.setEnabled(true);
            tBoxNome.requestFocus();
            tBoxEndereco.setEnabled(true);
            tBoxCidade.setEnabled(true);
            cBoxUF.setEnabled(true);
            tBoxDDD.setEnabled(true);
            tBoxTelefone.setEnabled(true);
            tBoxCEP.setEnabled(true);
            tBoxSalBase.setEnabled(true);
            tBoxTaxaComissao.setEnabled(true);
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

